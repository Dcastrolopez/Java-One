
public class TestBucles {
	
	public static void main(String[] args) {
		
		for (int multiplicador = 1; multiplicador <= 12; multiplicador++) {
			for (int contador = 0; contador <= 12; contador++) {
				System.out.print(multiplicador * contador);
				System.out.print(" ");
			}
			System.out.println();
		}
		
	}
	
	
	
}
